"use client";

import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";
import { z } from "zod";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Form, FormControl, FormField, FormItem, FormLabel, FormMessage } from "@/components/ui/form";
import { entitySchema } from "@/lib/master-data/schemas";

type EntityFormValues = z.input<typeof entitySchema>;
type EntityOutput = z.infer<typeof entitySchema>;

interface EntityFormProps {
  defaultValues?: Partial<EntityFormValues>;
  onSubmit: (data: EntityOutput) => Promise<void>;
  isSubmitting?: boolean;
}

export function EntityForm({ defaultValues, onSubmit, isSubmitting }: EntityFormProps) {
  const form = useForm<EntityFormValues, unknown, EntityOutput>({
    resolver: zodResolver(entitySchema),
    defaultValues: {
      name: "",
      type: "company",
      note: "",
      ...defaultValues,
    },
  });

  return (
    <Form {...form}>
      <form onSubmit={form.handleSubmit(onSubmit)} className="space-y-4">
        <FormField
          control={form.control}
          name="name"
          render={({ field }) => (
            <FormItem>
              <FormLabel>Tên chủ thể</FormLabel>
              <FormControl>
                <Input placeholder="Nhập tên chủ thể" {...field} />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />

        <FormField
          control={form.control}
          name="type"
          render={({ field }) => (
            <FormItem>
              <FormLabel>Loại</FormLabel>
              <FormControl>
                <select
                  {...field}
                  className="flex h-9 w-full rounded-md border border-input bg-transparent px-3 py-1 text-sm shadow-sm focus-visible:outline-none focus-visible:ring-1 focus-visible:ring-ring"
                >
                  <option value="company">Công ty</option>
                  <option value="person">Cá nhân</option>
                </select>
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />

        <FormField
          control={form.control}
          name="note"
          render={({ field }) => (
            <FormItem>
              <FormLabel>Ghi chú</FormLabel>
              <FormControl>
                <Input placeholder="Ghi chú (tùy chọn)" {...field} />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />

        <div className="flex justify-end gap-2 pt-2">
          <Button type="submit" disabled={isSubmitting}>
            {isSubmitting ? "Đang lưu..." : "Lưu"}
          </Button>
        </div>
      </form>
    </Form>
  );
}
